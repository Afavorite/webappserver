package com.demo.bean;

public class RPi_Control {
    private String control_flag;
    private String control_ster;
    private String control_lock;
    private String control_temp;
    private String control_user;

    public String getControl_flag() {
        return control_flag;
    }

    public void setControl_flag(String control_flag) {
        this.control_flag = control_flag;
    }

    public String getControl_ster() {
        return control_ster;
    }

    public void setControl_ster(String control_ster) {
        this.control_ster = control_ster;
    }

    public String getControl_lock() {
        return control_lock;
    }

    public void setControl_lock(String control_lock) {
        this.control_lock = control_lock;
    }

    public String getControl_temp() {
        return control_temp;
    }

    public void setControl_temp(String control_temp) {
        this.control_temp = control_temp;
    }

    public String getControl_user() {
        return control_user;
    }

    public void setControl_user(String control_user) {
        this.control_user = control_user;
    }
}
